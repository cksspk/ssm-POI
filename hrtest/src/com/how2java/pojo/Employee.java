package com.how2java.pojo;

/**
 * 职员的实体类
 * @author cksspk
 *
 */
public class Employee {
	private String strName;				//姓名
	private String strDepartment;		//所属部门
	private String strStation;			//岗位
	private String strPhoneNum;			//联系电话
	private String strUniversity;		//学校
	private String strMajor;			//专业
	private String strEducation;		//学历
	private String strGraduationTime;	//毕业时间
	private String strEntryTime;		//入职时间
	private String strPositiveTime;		//转正时间
	private String strStatus;			//状态
	private String strSeparationTime;	//离职时间
	
	
	public String getStrPhoneNum() {
		return strPhoneNum;
	}
	public void setStrPhoneNum(String strPhoneNum) {
		this.strPhoneNum = strPhoneNum;
	}
	public String getStrName() {
		return strName;
	}
	public void setStrName(String strName) {
		this.strName = strName;
	}
	public String getStrDepartment() {
		return strDepartment;
	}
	public void setStrDepartment(String strDepartment) {
		this.strDepartment = strDepartment;
	}
	public String getStrStation() {
		return strStation;
	}
	public void setStrStation(String strStation) {
		this.strStation = strStation;
	}
	
	public String getStrUniversity() {
		return strUniversity;
	}
	public void setStrUniversity(String strUniversity) {
		this.strUniversity = strUniversity;
	}
	public String getStrMajor() {
		return strMajor;
	}
	public void setStrMajor(String strMajor) {
		this.strMajor = strMajor;
	}
	public String getStrEducation() {
		return strEducation;
	}
	public void setStrEducation(String strEducation) {
		this.strEducation = strEducation;
	}
	public String getStrGraduationTime() {
		return strGraduationTime;
	}
	public void setStrGraduationTime(String strGraduationTime) {
		this.strGraduationTime = strGraduationTime;
	}
	public String getStrEntryTime() {
		return strEntryTime;
	}
	public void setStrEntryTime(String strEntryTime) {
		this.strEntryTime = strEntryTime;
	}
	public String getStrPositiveTime() {
		return strPositiveTime;
	}
	public void setStrPositiveTime(String strPositiveTime) {
		this.strPositiveTime = strPositiveTime;
	}
	public String getStrStatus() {
		return strStatus;
	}
	public void setStrStatus(String strStatus) {
		this.strStatus = strStatus;
	}
	public String getStrSeparationTime() {
		return strSeparationTime;
	}
	public void setStrSeparationTime(String strSeparationTime) {
		this.strSeparationTime = strSeparationTime;
	}
	
	
}
